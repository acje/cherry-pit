//! Guidelines output — human-first governance reference or setup guide.
//!
//! Two paths:
//! - No config: prescriptive quickstart showing how to set up ADR governance.
//! - Config present: complete governance reference derived from config +
//!   hardcoded rule catalog.
//!
//! Output targets humans reading a terminal, not LLMs. Uses headings,
//! bullet lists, and visual hierarchy.

use crate::config::Config;
use crate::model::{RelVerb, Tier};

