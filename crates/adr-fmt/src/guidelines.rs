// ── Lifecycle section ──────────────────────────────────────────────

fn print_lifecycle() {
    println!("LIFECYCLE");
    println!("─────────");
    println!();
    let statuses = [
        ("Draft", false),
        ("Proposed", false),
        ("Accepted", false),
        ("Rejected", true),
        ("Deprecated", true),
        ("Superseded by PREFIX-NNNN", true),
    ];
    for (name, terminal) in &statuses {
        let marker = if *terminal { " [terminal]" } else { "" };
        println!("  {name}{marker}");
    }
    println!();
    println!("  Terminal states require:");
    println!("    • Move ADR to stale directory");
    println!("    • Add a ## Retirement section");
    println!();
}

